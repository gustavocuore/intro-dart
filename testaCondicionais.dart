void main() {
  print("Testando condicionais");
  int idade = 18;

  if(idade >= 18) {
    print("Você é maior de idade, pode entrar!");
  }
  else {
    print("Você é menor de idade, não pode entrar!");
  }

  idade >= 18
  ? print("Você é maior de idade, pode entrar!")
  : print("Você é menor de idade, não pode entrar!");
}